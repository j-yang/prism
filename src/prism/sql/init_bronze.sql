-- ============================================================================
-- PRISM Bronze Schema Initialization
-- Version: 2.0
-- Description: Bronze layer stores raw data by original form structure
-- ============================================================================

-- Bronze layer is dynamically generated based on ALS parsing.
-- Each form from the source data becomes its own table in bronze schema.
--
-- Structure:
--   bronze.ae    -- Adverse Events form
--   bronze.cm    -- Concomitant Medications form
--   bronze.dm    -- Demographics form
--   bronze.lb    -- Laboratory form
--   bronze.vs    -- Vital Signs form
--   ... (N tables, one per form)
--
-- Each bronze table has:
--   - usubjid TEXT          -- Unique subject identifier
--   - subjid TEXT           -- Subject ID
--   - [form-specific columns] -- All fields from the original form
--
-- Data processing rules:
--   1. Column names: lowercase
--   2. Date conversion: SAS date → DATE type
--   3. No data transformation or filtering by default
--   4. Optional: User can specify columns to exclude
--
-- Metadata:
--   - meta.bronze_dictionary: Describes all variables (form_oid, schema, etc.)
--   - meta.form_classification: Maps form_oid to schema classification
--
-- Example DDL for a form (generated by ALS parser):
--
-- CREATE TABLE IF NOT EXISTS bronze.ae (
--     usubjid TEXT NOT NULL,
--     subjid TEXT,
--     aeterm TEXT,
--     aestdtc DATE,
--     aeendtc DATE,
--     aesoc TEXT,
--     aedecod TEXT,
--     aeout TEXT,
--     aeser TEXT,
--     ... (all other AE fields)
-- );
--
-- CREATE TABLE IF NOT EXISTS bronze.dm (
--     usubjid TEXT NOT NULL,
--     subjid TEXT,
--     age INTEGER,
--     sex TEXT,
--     race TEXT,
--     site TEXT,
--     country TEXT,
--     ... (all other DM fields)
-- );

CREATE SCHEMA IF NOT EXISTS bronze;

SELECT 'PRISM Bronze Schema initialized. Tables will be created by ALS parser.' AS status;
